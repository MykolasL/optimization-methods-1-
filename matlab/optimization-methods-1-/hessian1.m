function H=hessian1(x)
x1 = x(1);
x2 = x(2);

H(1,1) = 2*(x2 - 1)^2 + ...
         2*(x2^2 - 1)^2 + ...
         2*(x2^3 - 1)^2;

H(1,2) = 2*(x1*(x2 - 1) + (1.5 - x1 + x1*x2)) + ...
         2*(2*x1*x2*(x2^2 - 1) + 2*x2*(2.25 - x1 + x1*x2^2)) + ...
         2*(3*x1*x2^2*(x2^3 - 1) + 3*x2^2*(2.625 - x1 + x1*x2^3));

H(2,1) = H(1,2);

H(2,2) = 2*x1^2 + ...
         2*((2*x1*x2)^2 + 2*x1*(2.25 - x1 + x1*x2^2)) + ...
         2*((3*x1*x2^2)^2 + 6*x1*x2*(2.625 - x1 + x1*x2^3));
end