function g=gradient1(x)
x1 = x(1);
x2 = x(2);

g(1) = 2*(1.5 - x1 + x1*x2)*(x2 - 1) + ...
       2*(2.25 - x1 + x1*x2^2)*(x2^2 - 1) + ...
       2*(2.625 - x1 + x1*x2^3)*(x2^3 - 1);

g(2) = 2*x1*(1.5 - x1 + x1*x2) + ...
       4*x1*x2*(2.25 - x1 + x1*x2^2) + ...
       6*x1*x2^2*(2.625 - x1 + x1*x2^3);
end